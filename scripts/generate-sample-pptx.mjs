#!/usr/bin/env node
import fs from "node:fs/promises";
import path from "node:path";
import PptxGenJS from "pptxgenjs";

const parseArgs = () => {
  const args = process.argv.slice(2);
  /** @type {{ out?: string; slides?: number }} */
  const parsed = {};

  for (let i = 0; i < args.length; i += 1) {
    const arg = args[i];
    if (arg === "--out") {
      parsed.out = args[i + 1];
      i += 1;
      continue;
    }
    if (arg === "--slides") {
      const next = Number(args[i + 1]);
      if (!Number.isFinite(next) || next <= 0) {
        throw new Error("--slides must be a positive number");
      }
      parsed.slides = Math.max(1, Math.min(10, Math.floor(next)));
      i += 1;
      continue;
    }
    if (arg === "--help" || arg === "-h") {
      console.log("Usage: node scripts/generate-sample-pptx.mjs --out <path> [--slides <n>]");
      process.exit(0);
    }
  }

  if (!parsed.out) {
    throw new Error("--out is required");
  }
  if (!parsed.slides) {
    parsed.slides = 3;
  }
  return parsed;
};

